package com.viu.giin;

public class Operators {


	public static void main(String[] args){
		
	}
	/**
	 * Sums operands a and b
	 * @param a
	 * @param b
	 * @return
	 */
	public int sum(int a, int b) {
		int result = 0;
		
		result = a + b;
		
		return result;
	}
	
	/**
	 * Substracts operand b from a
	 * @param a
	 * @param b
	 * @return
	 */
	public double substraction(double a, double b) {
		double result = 0;
		
		result = a - b;
		
		return result;
	}
}
